package edu.uaslp.library.model;

public class Employee {
    private String name;
    private String code;

    //methods
    //Setters
    public void setName(String name) {
        this.name = name;
    }

    public void setCode(String code) {
        this.code = code;
    }

    //Getters

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }
}
